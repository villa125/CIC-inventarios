    private void createConnection() throws IOException{
        File archivo = new File("conexion.txt");
        BufferedWriter bw;
        if(archivo.exists()) {
            bw = new BufferedWriter(new FileWriter(archivo));
            bw.write("");
        } else {
            bw = new BufferedWriter(new FileWriter(archivo));
            bw.write("Acabo de crear el fichero de texto.");
        }
        bw.close();
    }
    
    
    private void ConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConnectActionPerformed
        // TODO add your handling code here:
        String user="";
        String pass="";
        try{
            view v = new view();
            Connection con = v.getConnection();
            Statement state = con.createStatement();
            String Comando = "select * from datosUsuario where username='"+ username.getText() + "' and password='"+password.getText()+"'";
            ResultSet rs = state.executeQuery(Comando);

            while(rs.next()){
                user = rs.getString("username");
                pass = rs.getString("password");

            }

            if(user.equals(username.getText())&& pass.equals(password.getText())){
                this.dispose();
                view vista = new view();
                vista.show();
            }else{
                JOptionPane.showMessageDialog(null, "Usuario o Password Incorrectos. Hable con el administrador");
            }
        }catch(SQLException ex) {
            Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_ConnectActionPerformed

